    INTO job_grade (Grade_level, lowest_sal, highest_sal) VALUES ('A', 0, 1000)
    INTO job_grade (Grade_level, lowest_sal, highest_sal) VALUES ('B', 1001, 2000)
    INTO job_grade (Grade_level, lowest_sal, highest_sal) VALUES ('C', 2001, 3000)
    INTO job_grade (Grade_level, lowest_sal, highest_sal) VALUES ('D', 3001, 4000)
    INTO job_grade (Grade_level, lowest_sal, highest_sal) VALUES ('E', 4001, 5000)
SELECT * FROM DUAL;

SELECT E.ENAME, E.SAL, J.GRADE_LEVEL FROM EMP E JOIN JOB_GRADE J ON E.SAL BETWEEN J.LOWEST_SAL AND J.HIGHEST_SAL;
SELECT E.ENAME, E.SAL, J.GRADE_LEVEL FROM EMP E JOIN JOB_GRADE J ON E.SAL BETWEEN J.LOWEST_SAL AND J.HIGHEST_SAL ORDER BY J.GRADE_LEVEL;

