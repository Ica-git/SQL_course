SELECT ENAME, JOB, (CASE JOB 
WHEN 'PRESIDENT' THEN 'GREAT'
WHEN 'MANAGER' THEN 'GOOD'
WHEN 'ANALYST' THEN 'BROOOO!'
WHEN 'SALESMAN' THEN 'HE OK'
ELSE 'MEH' END) AS BARZ FROM EMP;

SELECT ENAME, JOB, TO_CHAR(SAL, '$999,999.99'),
CASE WHEN JOB = 'MANAGER' THEN 'GOOD'
ELSE 'BAD' 
END AS SALLLL FROM EMP;

SELECT ENAME, JOB, TO_CHAR(SAL, '$999,999.99'),
(CASE WHEN SAL > 3000 THEN 'GOOD' ELSE 'BAD' END) FROM EMP;

SELECT ENAME, JOB, DEPTNO, DNAME, LOC, 
(CASE LOC WHEN 'NEW YORK' THEN 'NY'
WHEN 'DALLAS' THEN 'DA'
WHEN 'CHICAGO' THEN 'CH'
ELSE 'BO'
END) AS SHORTY
FROM EMP NATURAL JOIN DEPT;
