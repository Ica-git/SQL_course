SELECT E.ENAME, M.ENAME AS MANAGER, E.MGR FROM EMP E LEFT JOIN EMP M ON E.MGR = M.EMPNO;

SELECT ENAME, JOB, EMP.DEPTNO, DNAME, LOC FROM EMP, DEPT WHERE EMP.DEPTNO = DEPT.DEPTNO;
